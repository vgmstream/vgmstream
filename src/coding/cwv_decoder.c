/* .CWV DPCM, decompiled from the demo main (71004EB8B0)
 * Some info from: https://github.com/conhlee/cwvtool/
 */
void decode_cwv(VGMSTREAMCHANNEL* stream, sample_t* outbuf, int channelspacing, int32_t first_sample, int32_t samples_to_do) {
    int sample_pos = 0;
    int32_t hist = stream->adpcm_history1_32;
    off_t frame_offset = stream->offset; // frame size is 1

    for (int i = first_sample; i < first_sample + samples_to_do; i++) {
        uint8_t index = read_u8(frame_offset + i, stream->streamfile);

        hist += cwv_steps[index];

        outbuf[sample_pos] = hist; //no clamp
        sample_pos += channelspacing;
    }

    stream->adpcm_history1_32 = hist;
}
